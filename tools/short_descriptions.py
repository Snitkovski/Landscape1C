def apply(path: Path):
    js = path.read_text(encoding='utf-8')
    head = js[:js.index('window.LANDSCAPE')]
    data = json.loads(js[js.index('{'):js.rindex('}') + 1])
    items = data['items']
    assert len(items) == 63, f'{path}: ожидалось 63 карточки, найдено {len(items)}'
    for idx, short in SHORT.items():
        items[idx]['description'] = short
    out = head + 'window.LANDSCAPE = ' + json.dumps(data, ensure_ascii=False, indent=2) + ';\n'
    path.write_text(out, encoding='utf-8')
    longest = max(len(s) for s in SHORT.values())
    print(f'{path}: обновлено 63 описаний (макс. длина {longest} симв.)')


def main():
    for p in ('app/data.js', 'app/data.generated.js'):
        path = Path(p)
        if path.exists():
            apply(path)


if __name__ == '__main__':
    main()
