// Ряды кнопок из плоского списка значений (для осей онбординга)
const btnRows = (labels, prefix, perRow = 2) => {
    const rows = [];
    labels.forEach((l, i) => {
        if (i % perRow === 0) rows.push([]);
        rows[rows.length - 1].push({
            text: l,
            callback_data: `${prefix}:${l}`,
        });
    });
    return rows;
};

